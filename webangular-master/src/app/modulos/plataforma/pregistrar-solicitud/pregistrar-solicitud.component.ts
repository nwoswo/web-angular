  {
    this.soliService.registrarSolicitud(this.myForm.value)
    .subscribe(transacciono => {

      if (transacciono.sCOD == '0000') {
        this.msjResult = transacciono.sCOD;
        this.event.emit(this.msjResult);
        this.toastr.success("Operación Completada");
      } else {
        this.event.emit('0001');
        this.toastr.error(transacciono.sCOD + transacciono.sDESCOD);
      }
    },
      error => {
        this.event.emit('0005');
        this.toastr.error(error);
      }
    );
  }

